public class MonthObject {

    String item_name;
    Boolean isexpense;
    int quantity;
    int sum_of_one;

    MonthObject(String item_name, Boolean isexpense, int quantity, int sum_of_one) {
        this.item_name = item_name;
        this.isexpense = isexpense;
        this.quantity = quantity;
        this.sum_of_one = sum_of_one;
    }

}
